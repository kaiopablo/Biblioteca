    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        AutorTableModel tabela = (AutorTableModel)(tbDados.getModel());
        listaAutoria.clear();
        int i, qtd = tabela.getDados().size();
        for(i=0; i<qtd; i++)
        {
            int selection[] = tbDados.getSelectedRows();
            int j, qtdj = selection.length;
            for(j=0; j<qtdj; j++)
            {
                if(selection[j] == i){
                    Autor aut = (Autor)tabela.getDados().get(i);
                    Autoria autoria = new Autoria();
                    autoria.setAutor(aut);
                    listaAutoria.add(autoria);
                    break;
                }
            }
        }
        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed


