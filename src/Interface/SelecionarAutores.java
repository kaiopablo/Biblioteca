    private void atualizarTabela()
    {
        AutorTableModel tabela = (AutorTableModel)(tbDados.getModel());
        tabela.setDados(autorController.getListAutores(txtBuscar.getText()));
        tabela.fireTableDataChanged();
        
        ListSelectionModel model = tbDados.getSelectionModel();
        model.clearSelection();
        
        int i, qtd = tabela.getDados().size();
        for(Autoria aut : listaAutoria){
            for(i=0; i<qtd; i++){
                if(aut.getAutor().getId() == tabela.getDados().get(i).getId()){
                    model.addSelectionInterval(i, i);
                    break;
                }
            }
        }
    }
    
